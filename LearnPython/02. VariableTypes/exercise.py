# using of string, float & integer

myString = "Hello"
myFloat = 10.0
myInt = 20

# using if rules & %s, %f, %d
if myString == "Hello":
    print("String: %s" % myString)
if isinstance(myFloat, float) and myFloat == 10.0:
    print("Float: %f" % myFloat)
if isinstance(myInt, int) and myInt == 20:
    print("Integer: %d" % myInt)
